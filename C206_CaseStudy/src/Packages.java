public class Packages {

	private int code;
	private String desc;
	private String start;
	private String end;
	private String amount;

	public Packages(int code, String desc, String start, String end, String amount) {
		this.code = code;
		this.desc = desc;
		this.start = start;
		this.end = end;
		this.amount = amount;
	}

	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}


	
	
	
}
